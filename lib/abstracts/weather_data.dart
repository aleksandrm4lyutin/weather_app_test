
class WeatherData {
  final int dt;
  final int minTemp;
  final int maxTemp;
  final int averageTemp;
  final int humidity;
  final int wind;
  final String name;
  final int weatherCode;

  WeatherData({
    required this.dt,
    required this.minTemp,
    required this.maxTemp,
    required this.averageTemp,
    required this.humidity,
    required this.wind,
    required this.name,
    required this.weatherCode,
  });

}